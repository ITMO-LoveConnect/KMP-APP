package ru.connect.core.ui.theme

object ConnectContentAlpha {
    /**
     * low level of content alpha, typically used for disabled content
     */
    const val Light: Float = 0.3f

    /**
     * medium level of content alpha, used to represent medium emphasis content
     */
    const val Medium: Float = 0.6f

    /**
     * high level of content alpha, used to represent high emphasis content
     */
    const val High: Float = 1f
}
